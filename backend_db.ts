export function initDb() {
  try {
    if (fs.existsSync(DB_PATH)) {
      const data = fs.readFileSync(DB_PATH, "utf-8");
      db = JSON.parse(data);
    } else {
      db = getInitialData();
      saveDb();
    }
    console.log("[Node DB] Database initialized successfully.");
  } catch (error) {
    console.error("[Node DB] Error initializing database:", error);
    db = getInitialData();
  }
}

export function saveDb() {
  try {
    fs.writeFileSync(DB_PATH, JSON.stringify(db, null, 2), "utf-8");
  } catch (error) {
    console.error("[Node DB] Error saving database:", error);
  }
}

export function getDocuments(type?: string): Document[] {
  if (type) {
    return db.documents.filter(doc => doc.type === type);
  }
  return db.documents;
}

export function getDocument(id: string): Document | undefined {
  return db.documents.find(doc => doc.id === id);
}

export function getComplianceCheck(documentId: string): ComplianceCheck | undefined {
  return db.compliance_checks.find(chk => chk.document_id === documentId);
}

export function insertDocument(doc: Document) {
  db.documents.unshift(doc);
  saveDb();
}

export function insertComplianceCheck(chk: ComplianceCheck) {
  db.compliance_checks.unshift(chk);
  saveDb();
}

export function addAuditTrailEvent(docId: string, event_text: string, user: string = "GxPilot AI") {
  const doc = getDocument(docId);
  if (doc) {
    if (!doc.audit_trail) doc.audit_trail = [];
    doc.audit_trail.push({
      time: new Date().toISOString(),
      user,
      event: event_text
    });
    doc.updated_at = new Date().toISOString();
    saveDb();
  }
}

export function updateDocumentStatus(docId: string, status: string, reviewerName: string) {
  const doc = getDocument(docId);
  if (doc) {
    doc.status = status;
    doc.updated_at = new Date().toISOString();
    addAuditTrailEvent(docId, `Document status changed to ${status} via physical signoff by ${reviewerName} (21 CFR Part 11 compliant digital signature)`, reviewerName);
    saveDb();
  }
}

export function deleteDocumentAndReviews(docId: string) {
  db.documents = db.documents.filter(doc => doc.id !== docId);
  db.compliance_checks = db.compliance_checks.filter(chk => chk.document_id !== docId);
  saveDb();
}

export function getChatHistory(): ChatMessage[] {
  return db.chat_history;
}

export function insertChatMessage(role: "user" | "assistant", content: string) {
  const msg: ChatMessage = {
    id: `msg-${Math.random().toString(36).substr(2, 9)}`,
    role,
    content,
    timestamp: new Date().toISOString()
  };
  db.chat_history.push(msg);
  saveDb();
  return msg;
}

export function clearChatHistory() {
  db.chat_history = [];
  saveDb();
}

export function getRoiSummary() {
  const total_hours = db.roi_metrics.reduce((acc, curr) => acc + curr.hours_saved, 0);
  const total_errors = db.roi_metrics.reduce((acc, curr) => acc + curr.errors_prevented, 0);
  const total_usd = db.roi_metrics.reduce((acc, curr) => acc + curr.cost_saved_usd, 0);
  
  return {
    summary: {
      total_hours_saved: total_hours,
      total_errors_prevented: total_errors,
      total_cost_saved_usd: total_usd,
      total_tasks_assisted: db.roi_metrics.length,
      hourly_rate_assumption: 50.0
    },
    history: db.roi_metrics
  };
}

export function insertRoiMetric(task_type: string, hours_saved: number, errors_prevented: number, cost_saved_usd: number) {
  const metric: RoiMetric = {
    id: `roi-${Math.random().toString(36).substr(2, 9)}`,
    task_type,
    hours_saved,
    errors_prevented,
    cost_saved_usd,
    created_at: new Date().toISOString()
  };
  db.roi_metrics.unshift(metric);
  saveDb();
  return metric;
}
