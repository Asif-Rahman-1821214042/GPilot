import fs from "fs";
import path from "path";

const DB_PATH = path.join(process.cwd(), "gxpilot.json");

export interface AuditTrailEvent {
  time: string;
  user: string;
  event: string;
}

export interface Document {
  id: string;
  name: string;
  type: string; // 'SOP', 'APQR', 'MBR', 'EBR', 'General'
  status: string; // 'Draft', 'Under_Review', 'Approved', 'Rejected'
  content: string;
  content_type: string;
  file_size: number;
  audit_trail: AuditTrailEvent[];
  created_at: string;
  updated_at: string;
}

export interface Anomaly {
  type: string;
  severity: "High" | "Medium" | "Low";
  message: string;
}

export interface ChecklistItem {
  item: string;
  status: "PASS" | "FAIL" | "N/A";
}

export interface ComplianceCheck {
  id: string;
  document_id: string;
  score: number;
  status: "Approved" | "Flagged" | "Rejected";
  anomalies: Anomaly[];
  recommendations: string[];
  checklist: ChecklistItem[];
  created_at: string;
}

export interface ChatMessage {
  id: string;
  role: "user" | "assistant";
  content: string;
  timestamp: string;
}

export interface RoiMetric {
  id: string;
  task_type: string;
  hours_saved: number;
  errors_prevented: number;
  cost_saved_usd: number;
  created_at: string;
}

interface DatabaseSchema {
  documents: Document[];
  compliance_checks: ComplianceCheck[];
  chat_history: ChatMessage[];
  roi_metrics: RoiMetric[];
}

